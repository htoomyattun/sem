package com.napier.sem;

public class Capital {


    //    capital city name
    private String name;

    //    capital city country
    private String country;

    //    capital city district
    private String district;

    //     capital city population
    private int population;

    //set capital name
    public void setName(String cname){
        name = cname;
    }

    //get capital name
    public String getName(){
        return name;
    }

    //set capital country
    public void setCountry(String ccountry){
        country = ccountry;
    }

    //get capital country
    public String getCountry(){
        return country;
    }

    //set capital district
    public void setDistrict(String cdistrict){
        district = cdistrict;
    }

    //get capital district
    public String getDistrict(){
        return district;
    }

    //set capital population
    public void setPopulation(int cpopulation){
        population = cpopulation;
    }
    //get capital population
    public int getPopulation() {
        return population;
    }

    }

